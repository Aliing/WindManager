AeroHive =
{
  metric: function (display)
  {
    return '[' + display + ']';
  },

  axis: function (metrics)
  {
    for( metric in metrics )
      return metric;
  },
  metrics: function (report,metric)
  {
    if( REPORT_DATA == report) {
    	return AeroHive.reportData[metric];
    }
    return AeroHive.localizationData[metric];
  },
  localization: function (axis)
  {
	  return AeroHive.localizationData[axis];
  },

  replace: function (expression,global,replacement)
  {
	if (!expression || !global) {
		return "";
	}
    for( replaced = expression.replace( global, replacement) ; replaced != expression ;)
        replaced = (expression = replaced).replace( global, replacement);
      return expression;
  },
  validate: function (expression,axis, retTypes)
  {
	 if(!expression || expression.indexOf("[")<0){
		 return;
	 }
		
    var rowConfig = AeroHive.localization( axis);
    var metricsVal = [{
    	key: axis,
    	obj: rowConfig
    }];
    
	if (rowConfig.yaxis) {
		for (var yKey in rowConfig.yaxis) {
			metricsVal.push({
				key: yKey,
				obj: rowConfig.yaxis[yKey]
			});
		}
	}
    for(var i = 0; i < metricsVal.length; i++)
    {
      var metricTmp = metricsVal[i];
      var display = AeroHive.metric(metricTmp.obj.prompt);
      expression = AeroHive.replace
      (
        AeroHive.replace(expression, "AHAVG(" + display, 'averageAH(rows,0,"' + metricTmp.key + '"'),
        display,
        '["' + metricTmp.key + '"]'
      );
    }
    expression = expression.replace(/\[/g, "row[");
    var row = AeroHive.metrics( REPORT_DATA, axis),
    		rows = [row];
    try{
    	var result = eval( expression);
        if(result!=undefined) {
        	var resultType = typeof result;
        	if (retTypes && retTypes.length > 0) {
        		for (var i = 0; i < retTypes.length; i++) {
        			if (resultType == retTypes[i]) {
        				break;
        			}
        		}
        		if (i == retTypes.length) {
        			resultType = undefined;
        		}
        	}
        	if (resultType === 'string') {
        		return expression;
        	}
        	if (resultType === 'number') {
	        	if(isNaN(result) || !isFinite(result)){
	        		return;
	        	}
	        	return expression;
        	}
        }
    }catch(e){
    	return;
    }
  },

  average: function (begin,end,rows,metric)
  {
    var sum = 0;
    for( index = end ; begin != index ;)
      sum += rows[ --index ][ metric ];
    return sum / (end - begin);
  },
  evaluate: function (validated,rows,row,cache)
  {
    return eval( validated);  //TODO  breakDowns
  },

  text: function (option,display)
  {
    option.appendChild
    (
       document.createTextNode( display)
    );
  }
};

function averageAH (rows,row,metric,samples)
{
  var length = rows.length;
  if( samples
   && samples < length )
  {
    var begin = samples >>> 1;
    if( begin <= row )
    {
      var end = (begin = row - begin) + samples;
      return end > length
           ? AeroHive.average( length - samples, length, rows, metric)
           : AeroHive.average( begin, end, rows, metric);
    }
    length = samples;
  }
  return AeroHive.average( 0, length, rows, metric);
}


AeroHive.reportData = null;
AeroHive.localizationData = null;
AeroHive.prepareReportAndLocalizationData = function() {
	if (AeroHive.reportData
			&& AeroHive.localizationData) {
		return;
	}
	
	AeroHive.reportData = {};
	AeroHive.localizationData = {};
	
	var keys = [];
	if (REPORT_LOCALIZATION && REPORT_LOCALIZATION.length > 0) {
		for (var i = 0; i < REPORT_LOCALIZATION.length; i++) {
			var localizationVal = REPORT_LOCALIZATION[i];
			if (localizationVal) {
				for (var key in localizationVal) {
					AeroHive.localizationData[key] = localizationVal[key];
					keys.push(key);
					break;
				}
			}
		}
	}
	
	if (REPORT_DATA && REPORT_DATA.length > 0 && keys.length > 0) {
		for (var i = 0; i < keys.length; i++) {
			var keyVal = keys[i];
			for (var j = 0; j < REPORT_DATA.length; j++) {
				var rpDataVal = REPORT_DATA[j];
				if (!rpDataVal || rpDataVal.length < 1) {
					continue;
				}
				rpDataVal = rpDataVal[0];
				for (var key in rpDataVal) {
					if (key == keyVal) {
						AeroHive.reportData[key] = rpDataVal;
						break;
					}
				}
			}
		}
	}
};
AeroHive.rePrepareReportAndLocalizationData = function() {
	AeroHive.reportData = null;
	AeroHive.localizationData = null;
	AeroHive.prepareReportAndLocalizationData();
};
