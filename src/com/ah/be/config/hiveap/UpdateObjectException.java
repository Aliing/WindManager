package com.ah.be.config.hiveap;

public class UpdateObjectException extends Exception {

	/**
	 *
	 */
	private static final long serialVersionUID = 1L;

	public UpdateObjectException() {
		super();
	}

	public UpdateObjectException(String message) {
		super(message);
	}

	public UpdateObjectException(Throwable cause) {
		super(cause);
	}

	public UpdateObjectException(String message, Throwable cause) {
		super(message, cause);
	}

}
