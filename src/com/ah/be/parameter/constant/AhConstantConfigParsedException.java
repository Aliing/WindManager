package com.ah.be.parameter.constant;

public class AhConstantConfigParsedException extends Exception {

	private static final long serialVersionUID = -1;

	public AhConstantConfigParsedException() {
		super();
	}

	public AhConstantConfigParsedException(String message) {
		super(message);
	}

	public AhConstantConfigParsedException(String message, Throwable cause) {
		super(message, cause);
	}

	public AhConstantConfigParsedException(Throwable cause) {
		super(cause);
	}

}